package modul_1;

public class CekGenapGanjil {

    public static void main(String[] args) {

        int angka = 7;

        if (angka % 2 == 0) {
            System.out.println("Bilangan " + angka + " adalah GENAP");
        } else {
            System.out.println("Bilangan " + angka + " adalah GANJIL");
        }

    }
}